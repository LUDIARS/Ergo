#pragma once

#include <cstdint>
#include <string>
#include <vector>
#include <functional>
#include <any>

namespace ergo::event {

// =====================================================================
// イベントID型
// =====================================================================
using EventId = uint32_t;
using ListenerId = uint64_t;

// =====================================================================
// 優先度
// =====================================================================
enum class Priority : uint8_t {
    Low     = 0,
    Normal  = 128,
    High    = 255
};

// =====================================================================
// イベント
// =====================================================================
struct Event {
    EventId             id          = 0;
    Priority            priority    = Priority::Normal;
    std::any            payload;
    uint32_t            delayFrames = 0;    // 遅延フレーム数（0 = 即時）
};

// =====================================================================
// リスナーコールバック型
// =====================================================================
using EventCallback = std::function<void(const Event&)>;

// =====================================================================
// リスナー情報
// =====================================================================
struct Listener {
    ListenerId      listenerId  = 0;
    EventId         eventId     = 0;
    EventCallback   callback;
};

// =====================================================================
// シーケンサー設定
// =====================================================================
struct SequencerConfig {
    uint32_t    maxProcessPerFrame  = 64;    // フレームあたり最大処理数
    uint32_t    maxQueueSize        = 4096;  // キュー最大サイズ
};

} // namespace ergo::event
